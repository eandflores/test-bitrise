package com.aim.atlas.core.valueObjects

import com.aim.atlas.core.throwable.InvalidEmailException

internal class Email(var value: String) {
    private val regex = Regex("^(([\\w-]+\\.)+[\\w-]+|([a-zA-Z]|[\\w-]{2,}))@"
            + "((([0-1]?[0-9]{1,2}|25[0-5]|2[0-4][0-9])\\.([0-1]?"
            + "[0-9]{1,2}|25[0-5]|2[0-4][0-9])\\."
            + "([0-1]?[0-9]{1,2}|25[0-5]|2[0-4][0-9])\\.([0-1]?"
            + "[0-9]{1,2}|25[0-5]|2[0-4][0-9]))|"
            + "([a-zA-Z]+[\\w-]+\\.)+[a-zA-Z]{2,4})$")
    init {
        val match = regex.matches(value)
        if (match === false) {
            throw InvalidEmailException(value)
        }
    }
}